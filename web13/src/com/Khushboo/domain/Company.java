package com.Khushboo.domain;

public class Company {
	String comp_uid;
	String comp_name;
	String comp_email;
	String comp_phn;
	String comp_website;
	public String getComp_uid() {
		return comp_uid;
	}
	public void setComp_uid(String comp_uid) {
		this.comp_uid = comp_uid;
	}
	public String getComp_name() {
		return comp_name;
	}
	public void setComp_name(String comp_name) {
		this.comp_name = comp_name;
	}
	public String getComp_email() {
		return comp_email;
	}
	public void setComp_email(String comp_email) {
		this.comp_email = comp_email;
	}
	public String getComp_phn() {
		return comp_phn;
	}
	public void setComp_phn(String comp_phn) {
		this.comp_phn = comp_phn;
	}
	public String getComp_website() {
		return comp_website;
	}
	public void setComp_website(String comp_website) {
		this.comp_website = comp_website;
	}

}
